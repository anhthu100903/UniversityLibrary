    public void setUpTable() throws SQLException{
        bookNameTable.setRowCount(0);
        Vector<BookNameDTO> arr = bookBUS.getAllBookName();
        for(int i=0;i<arr.size();i++){
            BookNameDTO au = arr.get(i);
            Object row[] = {au.getId(),au.getName()};
            bookNameTable.addRow(row);
        }
    }

