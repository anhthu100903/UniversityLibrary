    private void addSupplierButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addSupplierButtonActionPerformed
        // TODO add your handling code here:
        int row = bookNameTable.getSelectedRow();
            if (row >= 0) {
                book.setId(Integer.parseInt(bookNameTable.getValueAt(row, 0).toString()));
                book.setName(bookNameTable.getValueAt(row, 1).toString());
                dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Vui lòng chọn tên sách");
            }
    }//GEN-LAST:event_addSupplierButtonActionPerformed

